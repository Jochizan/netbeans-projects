package clases_AlgotrimiaP;

public class Longitud_del_círculo {


    public static void main(String[] args) {

        double radio = 2.5, longitud = 0, Pi = 3.14159;
        
        longitud = 2*Pi*radio;
        
        System.out.println("La longitud del círculo es: " + longitud);
    }
    
}
