package clases_AlgotrimiaP;

public class Área_del_círculo {

    public static void main(String[] args) {
       
        double radio = 4.78, área = 0, Pi = 3.14159;
        
        área = Pi*Math.pow(radio, 2);
        
        System.out.println("El área del círculo es: " + área);
    }
    
}
